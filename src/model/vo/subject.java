package model.vo;

/**
 *
 * @author Markensi
 */
public class subject {
    char conAsignatura;
    String nombre, curso, profesor;

    public char getConAsignatura() {
        return conAsignatura;
    }

    public void setConAsignatura(char conAsignatura) {
        this.conAsignatura = conAsignatura;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }

    public String getProfesor() {
        return profesor;
    }

    public void setProfesor(String profesor) {
        this.profesor = profesor;
    }
    
    
}
